#include "ft_nm.h"

t_env		*init_env(void)
{
	t_env	*ret;

	if ((ret = malloc(sizeof(t_env))) != NULL)
	{
		ret->froot = NULL;
		ret->handler = NULL;
	}
	return (ret);
}
